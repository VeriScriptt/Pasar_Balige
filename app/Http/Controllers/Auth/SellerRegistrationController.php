    /**
     * Handle an incoming registration request for sellers.
     *
     * @throws \Illuminate\Validation\ValidationException
     */
    public function storeSeller(Request $request): RedirectResponse
    {
        $request->validate([
            'name' => ['required', 'string', 'max:255'],
            'email' => ['required', 'string', 'lowercase', 'email', 'max:255', 'unique:'.User::class],
            'password' => ['required', 'confirmed', Rules\Password::defaults()],
        ]);

        // Create a new user with the role 'seller'
        $user = User::create([
            'name' => $request->name,
            'email' => $request->email,
            'password' => Hash::make($request->password),
            'role' => 'penjual', // Set the role as 'seller'
        ]);

        // Fire the Registered event
        event(new Registered($user));

        // Log in the user
        Auth::login($user);

        // Redirect the user to the seller dashboard page
        return redirect()->route('penjual');
    }
